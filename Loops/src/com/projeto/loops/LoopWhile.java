package com.projeto.loops;

public class LoopWhile {

	public static void main(String[] args) {
		
		int numero=0;
		
		//Enquanto o numero for menor do que 5
		while( numero < 5 ) {
			System.out.println("Loop: " + numero++); //Printa na tela o Loop: + o valor do numero +1.
			// numero++; //Pega o valor de numero, soma +1 e printa na tela com o "Loop:" 
			// numero = numero + 1;
			
	}
  }	
}

