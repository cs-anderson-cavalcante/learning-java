package com.projeto.loops;

public class LoopFor {

	public static void main(String[] args) {
		
		for (int n=0 ; n < 5 ; n++) {
			System.out.println("Loop: " + n);
	   }
	}
}


/*
Assim como no While e no Do While,o FOR é um laço de repetição. Unica diferença é a declaração
 for (variável inicializada + verificação da condição +  incremento){ }
 */
